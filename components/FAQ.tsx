export function FAQ() {
  const categories = Array.from(new Set(faqItems.map(item => item.category)));

  return (
    <div className="space-y-8">
      <div className="text-center mb-8">
        <h2 className="text-3xl font-bold text-gray-900 mb-2">
          よくある質問（FAQ）
        </h2>
        <p className="text-gray-600">
          ファクタリングに関するよくある質問を{faqItems.length}項目まとめました
        </p>
      </div>

      {categories.map((category, categoryIndex) => {
        const categoryItems = faqItems.filter(item => item.category === category);
        return (
          <div key={categoryIndex} className="space-y-4">
            <h3 className="text-xl font-bold text-gray-900 border-b-2 border-blue-500 pb-2">
              {category}
            </h3>
            <Accordion type="single" collapsible className="w-full">
              {categoryItems.map((item, index) => {
                const globalIndex = faqItems.findIndex(
                  faq => faq.question === item.question
                );
                return (
                  <AccordionItem key={index} value={`item-${globalIndex}`}>
                    <AccordionTrigger className="text-left font-semibold">
                      Q{globalIndex + 1}. {item.question}
                    </AccordionTrigger>
                    <AccordionContent className="text-gray-700 leading-relaxed">
                      {item.answer}
                    </AccordionContent>
                  </AccordionItem>
                );
              })}
            </Accordion>
          </div>
        );
      })}
    </div>
  );
}
